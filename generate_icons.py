def to_bytes(rows):
    out = []
    for row in rows:
        b0 = 0
        b1 = 0
        for i, ch in enumerate(row):
            if ch == '#':
                if i < 8:
                    b0 |= 1 << (7 - i)
                else:
                    b1 |= 1 << (7 - (i - 8))
        out.append(f"0x{b0:02X}, 0x{b1:02X}")
    return out

