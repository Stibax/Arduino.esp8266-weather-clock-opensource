    f.write("#include <Arduino.h>\n\n")
    f.write("#define ICON_WIDTH 16\n")
    f.write("#define ICON_HEIGHT 16\n\n")
    for name, rows in ICONS.items():
        f.write(f"const uint8_t {name}[{len(rows)}*2] PROGMEM = {{\n  ")
        f.write(", ".join(to_bytes(rows)))
        f.write("\n};\n\n")
    f.write("#endif // ICONS_H\n")

